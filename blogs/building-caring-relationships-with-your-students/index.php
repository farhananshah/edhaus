      <p>Research says high expectations and building a sincere relationship with students is one of the keys to student achievement. As a teacher, I do believe this; however, we know it isn’t always reality. We will always encounter students who choose not to be successful, no matter how fair or caring a teacher behaves. While, we can’t change the things not in our control, we should always strive to be the best educators we can be.</p>

      <p>Teachers tend to form expectations of students early in the year, mostly based on articulations and students’ performance. Marzano believes, “as human beings, we cannot be expected to have a natural affinity for every student in class. However, we can be expected to behave in a way that communicates care and concern equally for every student.” He also suggests steps for self-analysis and reflection in order to establish successful teacher student relationship in the classroom:</p>

      <ol>
        <li>Consciously engage in behavior such as smiling and looking interested.</li>
        <li>Let students know your expectation level and make sure to determine if you are not biased because of ethnicity or socio-economic status.</li>
        <li>Make sure to let low-performing students that they are valued and respected.</li>
      </ol>

      <p>Scheduling Peer Visits and seeking assistance from teacher leaders in the building, along with constant (daily) reflection are some ways to continually assess student teacher interactions. Hopefully, this can slowly pave the way to reach those students who tend to slip through the cracks.</p>

